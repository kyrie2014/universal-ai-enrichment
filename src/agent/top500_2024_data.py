def is_in_top500(company_name: str) -> bool:
    """
    判断企业是否在2024中国民营企业500强榜单中
    
    Args:
        company_name: 公司名称（可以是全称或简称）
    
    Returns:
        bool: 是否在榜单中
    """
    if not company_name:
        return False
    
    company_name = company_name.strip()
    
    # 直接匹配
    if company_name in TOP500_2024_PRIVATE:
        return True
    
    # 使用别名匹配
    full_name = COMPANY_NAME_ALIASES.get(company_name)
    if full_name and full_name in TOP500_2024_PRIVATE:
        return True
    
    # 模糊匹配：检查是否包含榜单中的企业名称
    for top_company in TOP500_2024_PRIVATE:
        if company_name in top_company or top_company in company_name:
            if len(company_name) >= 3 or len(top_company) >= 3:  # 避免过短的匹配
                return True
    
    return False

def get_full_name(company_name: str) -> str:
    """
    获取企业的官方全称
    
    Args:
        company_name: 公司名称（可以是全称或简称）
    
    Returns:
        str: 官方全称（如果找不到则返回原名）
    """
    return COMPANY_NAME_ALIASES.get(company_name, company_name)

